    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        if (txtName.getText() != null && txtDescription.getText() != null  && txtPrice.getText() != null) {
            Plate plato = new Plate( txtName.getText(), txtPrice.getText(), txtDescription.getText(),CBType.getSelectedItem().toString());
            try {
                String res = servicioPlato.createPlate(plato);
                if (!res.equals("FALLO")) {
                    JOptionPane.showMessageDialog(null, "plato agregado con exito");
                    txtName.setText("");
                    txtPrice.setText("");
                    txtDescription.setText("");
                    
                }
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, "El plato ya esta registrado" + ex.getMessage());

            }
        }

    }//GEN-LAST:event_btnAgregarActionPerformed

